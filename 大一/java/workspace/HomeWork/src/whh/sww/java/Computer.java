package whh.sww.java;

public class Computer {
    public String brand;
    public String color;
    public double price;

    public Computer(String brand, String color, double price) {
        this.brand = brand;
        this.color = color;
        this.price = price;
    }
    public void setPrice(){
        this.price=price;
    }
    public double getPrice() {
        return price;
    }
    public void setBrand(){
        this.brand=brand;
    }
    public String getBrand() {
        return brand;
    }
    public void setColor(){
        this.color=color;
    }
    public String getColor() {
        return color;
    }

}


















